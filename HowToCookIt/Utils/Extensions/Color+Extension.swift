import Foundation

import SwiftUI

extension Color {
	
	static var emerald: Color {
		Color("emerald", bundle: .module)
	}
	
	static var blackGreen: Color {
		Color("darkGreen", bundle: .module)
	}
	
	static var steelBlue: Color {
		Color("blue", bundle: .module)
	}
	static var babyBlue: Color {
		Color("lightBlue", bundle: .module)
	}
	static var tropeGray: Color {
		Color("gray", bundle: .module)
	}
}
